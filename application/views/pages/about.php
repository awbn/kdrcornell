	          <h5>About &Kappa;&Delta;&Rho;</h5>
	          <p>The Beta Chapter of &Kappa;&Delta;&Rho; is a multi-faceted organization and as such, holds many values dear to her.  This page seeks to explain those values through our <a href="#motto" data-toggle="tab">motto</a>, <a href="#mission" data-toggle="tab">Mission Statement</a>, <a href="#precepts" data-toggle="tab">Precepts</a>, and <a href="#credo" data-toggle="tab">Credo</a>.  Membership in &Kappa;&Delta;&Rho; is for life, and we expect all members to uphold these values in every way possible.</p>
	        </div>
	        
	        
	        <div class="tab-pane" id="motto">
	          <h5>The Motto of &Kappa;&Delta;&Rho;</h5>
	          <p>The open motto of KDR is <em>Honor Super Omnia</em>, or Honor Above All Things.  Simply put, this means that members of Kappa Delta Rho are men of honor and will strive to uphold that honor in every aspect of their life.  We take our motto very seriously.</p>
	        </div>
	        
	        
	        <div class="tab-pane" id="mission">
	          	<h5>The Mission of the Beta Chapter of &Kappa;&Delta;&Rho;</h5>
	          	<p>The following statement of mission was written in 2002 and has been adopted as the guidelines to which we as a chapter focus our efforts.  This is the vision that we ask all the actives and alumni of Beta Chapter to adhere to and promote:</p>
				<blockquote>
				<p>The Kappa Delta Rho Pathfinder states that the Kappa Delta Rho fraternity is a lifetime brotherhood, dedicated to the continuing character development of its members through fellowship, leadership, scholarship, service, and tradition.</p>
				<p>We of the Beta Chapter of Kappa Delta Rho are committed to developing our brothers beyond the education that Cornell University offers and to molding them into Men of Honor.  For, as indicated by our Latin motto “Honor Super Omnia”, we hold Honor Above All Things.</p>
				<p>As young men enrolled at Cornell University, we commit ourselves to scholarship, using each day to educate ourselves in our chosen field.  However, though the rigors of college education develop us academically, our potential for growth goes beyond those lessons taught in the classroom.  This is where the Kappa Delta Rho Fraternity plays an important role in the growth and development of young men at Cornell.</p>
				<p>Being a brother of Kappa Delta Rho signifies much more than simply being a member of a national social Fraternity.  Though one of the goals of the fraternity is the social development of her brothers, there is a much larger perspective to Kappa Delta Rho, especially at the Beta Chapter.</p>
				<p>We are men who feel a bond beyond that of friendship.  We have come down the long path of life to a point that is critical to our future.  College life is often hectic and stressful.  By himself, a person may feel lost or unable to find himself socially, academically, or mentally.  By joining as brothers, we become a family, working together to develop our relationships with each other, with our university, and with the world.  Guided by fraternal spirit, we aid each other in our growth as men.</p>
				<p>In addition to personal growth, we are here to help the community around us.  Therefore we also commit ourselves to community service and the betterment of those around us.  We recognize the need to take responsibility for our actions, and as Men of Honor who follow this path.  We stick to our word, we stick to our friends, and we stick to our brothers.</p>
				<p>In sum, Kappa Delta Rho is a fraternity dedicated to developing young men into leaders, friends, and men of Honor.  We strive to be men who are held accountable for our actions, men who learn and adhere to responsibility, men who are considered gentlemen, and men who give back to society through our actions.</p>
				<p>We hope that all who join us in the fraternity of Kappa Delta Rho, Beta Chapter, will hold to our ideals and, through experience with our fraternity, become a different man: a better man, a man with vision and purpose, and above all things, a man of Honor.</p>
				</blockquote>
	        </div>

	        <div class="tab-pane" id="credo">
	          	<h5>The Credo of &Kappa;&Delta;&Rho;</h5>
	          	<p>Our Credo was written in 1946 by George E. Shaw, Alpha '10, the first pledge of &Kappa;&Delta;&Rho;, and expresses the sentiment of brotherhood:</p>
				<blockquote>
				<p>Since the Beginning, wherever men have come together, there have I been.</p>
				<p>My membership is legion. From the humble home and from the stately mansion; from the rolling farms and from the noisy factories; from the East and West and North and South have my followers come.</p>
				<p>They who understand my meaning have followed in the footsteps of the Man of Galilee. In peace, they have been their brother’s keeper, and in the agony of war, their blood has enriched the Fields of Flanders and reddened the sands of the Pacific.</p>
				<p>Their reward has been the Inspiration of their Youth; the Driving Power of their Manhood; the Memories of their Maturity. They have fought the good Fight and of these, my sons, I am justly proud,</p>
				<p>Who am I? My name is FRATERNITY. And because I have given Man that which he craves, I shall endure. My sons shall neither falter nor fail. They shall add new lustre to my name.</p>
				</blockquote>
	        </div>

	        <div class="tab-pane" id="precepts">
	        	<h5>The Precepts of &Kappa;&Delta;&Rho;</h5>
	          	<p>The Precepts were adopted by KDR's 95th National Convention in 2006 and express the expectations and responsibilities of being a member of the Kappa Delta Rho Fraternity:</p>
				<blockquote>
				<p>As a Brother of the National Fraternity of Kappa Delta Rho, I have but one aim: to lead an honorable life.</p>
				<p>To this end, I am bound by my words and actions to:</p>
				<p>Embrace the principles and spirit of the KDR Gentleman, treating all those I encounter with dignity and respect;</p>
				<p>Work diligently in the pursuit of my education, understanding that I am a student first and that the quest for knowledge will last a lifetime;</p>
				<p>Meet all of my obligations to the Fraternity in a timely manner, so as to ensure that I am doing my share and that I am not a burden to my brothers;</p>
				<p>Engage in the service of mankind, not for the praise or recognition that such service may bring, but because it is the right thing to do;</p>
				<p>Serve as my brothers' keeper, holding them accountable for their actions as they hold me accountable for mine, and</p>
				<p>Support my Alma mater, that she may view Kappa Delta Rho as a partner in the development of her students.</p>
				<p>I understand that membership in Kappa Delta Rho is a lifelong privilege that is contingent upon my willingness to incorporate the values of the Fraternity into my daily life and uphold the oath I have sworn. As I speak these words I once again affirm to my brothers, and all who hear me, that I am a KDR Gentleman and I will place Honor Above All Things.</p>
				<p>Honor Super Omnia!</p>
				</blockquote>
